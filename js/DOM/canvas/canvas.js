//! ШПАРГАЛКА ПО HTML5 Canvas 2D API
// 🔳 ⁡⁢⁣⁣ИНИЦИАЛИЗАЦИЯ⁡
{
	const canvas = document.getElementById("myCanvas");
	const ctx = canvas.getContext("2d"); // '2d' для 2D графики
}
// 🔳 ⁡⁢⁣⁣ПУТИ И ФИГУРЫ (Paths & Shapes)⁡
{
	// ⭐ ⁡⁣⁣⁢Рисование происходит по "путям". Нужно начать путь, нарисовать линии и затем закрасить или обвести.⁡
	// ⁡⁢⁣⁣МЕТОД 	                ОПИСАНИЕ⁡
	// tx.beginPath()           ⁡⁣⁣⁢Начинает новый путь (сбрасывает текущий).⁡
	// ctx.moveTo(x, y)         ⁡⁣⁣⁢Перемещает "перо" в точку (x, y) без рисования.⁡
	// ctx.lineTo(x, y)         ⁡⁣⁣⁢Рисует линию от текущей точки до (x, y).⁡
	// ctx.closePath()          ⁡⁣⁣⁢Замыкает путь (линия от конца к началу).⁡
	// ctx.stroke()             ⁡⁣⁣⁢Рисует контур пути (обводка).⁡
	// ctx.fill()               ⁡⁣⁣⁢Закрашивает внутреннюю область пути.⁡
	// ⭐ ⁡⁣⁣⁢Прямоугольники (быстрые методы):⁡
	// ☑️ ctx.fillRect(x, y, w, h) ⁡⁣⁣⁢— Залитый прямоугольник.⁡
	// ☑️ ctx.strokeRect(x, y, w, h) ⁡⁣⁣⁢— Контур прямоугольника.⁡
	// ☑️ ctx.clearRect(x, y, w, h) ⁡⁣⁣⁢— Очищает область (делает прозрачной).⁡
	// ⭐ ⁡⁣⁣⁢Круги и дуги:⁡
    // ☑️ ctx.arc(x, y, radius, startAngle, endAngle, counterClockwise) —⁡⁣⁣⁢ Углы в радианах! (360° = 2 * Math.PI).⁡
    {
		ctx.beginPath();
		ctx.moveTo(50, 50);
		ctx.lineTo(150, 50);
		ctx.lineTo(100, 150);
		ctx.closePath();
		ctx.fill(); // Треугольник
	}
}
// 🔳 ⁡⁢⁣⁣СТИЛИ (Styles)⁡
{
    // ⁡⁢⁣⁣СВОЙСТВО 	                ОПИСАНИЕ⁡
    // ctx.fillStyle                ⁡⁣⁣⁢Цвет заливки (hex, rgb, rgba, gradient).⁡
    // ctx.strokeStyle              ⁡⁣⁣⁢Цвет обводки.⁡
    // ctx.lineWidth                ⁡⁣⁣⁢Толщина линии (в пикселях).⁡
    // ctx.lineCap                  ⁡⁣⁣⁢Концы линий: 'butt', 'round', 'square'.⁡
    // ctx.lineJoin                 ⁡⁣⁣⁢Стыки линий: 'round', 'bevel', 'miter'.⁡
    // ctx.setLineDash([5, 15])     ⁡⁣⁣⁢Пунктирная линия (длина штриха, длина пробела).⁡
}
// 🔳 ⁡⁢⁣⁣ТЕКСТ (Text)⁡
{
    // ⁡⁢⁣⁣МЕТОД/СВОЙСТВО 	            ОПИСАНИЕ⁡
    // ctx.font                     ⁡⁣⁣⁢Шрифт (например: "30px Arial").⁡
    // ctx.textAlign                ⁡⁣⁣⁢Выравнивание: 'start', 'end', 'left', 'right', 'center'.⁡
    // ctx.textBaseline             ⁡⁣⁣⁢Базовая линия: 'top', 'middle', 'bottom', 'alphabetic'.⁡
    // ctx.fillText(text, x, y)     ⁡⁣⁣⁢Рисует залитый текст.⁡
    // ctx.strokeText(text, x, y)   ⁡⁣⁣⁢Рисует контур текста.⁡
    // ctx.measureText(text)        ⁡⁣⁣⁢Возвращает объект с шириной текста (width).⁡
}
// 🔳 ⁡⁢⁣⁣ИЗОБРАЖЕНИЯ (Images)⁡
{
	// ☑️ ctx.drawImage(image, dx, dy) ⁡⁣⁣⁢— Рисует картинку в точке (dx, dy).⁡
	// ☑️ ctx.drawImage(image, dx, dy, dWidth, dHeight) ⁡⁣⁣⁢— Рисует с изменением размера.⁡
	// ☑️ ctx.drawImage(image, sx, sy, sWidth, sHeight, dx, dy, dWidth, dHeight) ⁡⁣⁣⁢— Вырезает часть (crop) и рисует.⁡
	// ⚠️ ⁡⁣⁣⁢Примечание: image может быть <img>, <video> или другой <canvas>.⁡
}
// 🔳 ⁡⁢⁣⁣ТРАНСФОРМАЦИИ (Transformations)⁡
{
	// ⭐ ⁡⁣⁣⁢Изменение системы координат. ⚠️ Важно: влияет на всё, что рисуется после вызова.⁡
    // ⁡⁢⁣⁣МЕТОД	                            ОПИСАНИЕ⁡
    // ctx.translate(x, y)          ⁡⁣⁣⁢        Сдвигает начало координат (0,0) в точку (x, y).⁡
    // ctx.rotate(angle)            ⁡⁣⁣        ⁡⁣⁣⁢Поворачивает холст на угол (в радианах).⁡
    // ctx.scale(x, y)              ⁡⁣⁣⁢        Масштабирует (x — по горизонтали, y — по вертикали).⁡
    // ctx.transform(a, b, c, d, e, f)      ⁡⁣⁣⁢Прямое изменение матрицы трансформации.⁡
    // ctx.setTransform(a, b, c, d, e, f)   ⁡⁣⁣⁢Сбрасывает матрицу и устанавливает новую.⁡
    // ctx.resetTransform()                 ⁡⁣⁣⁢Сбрасывает все трансформации к исходным.⁡
}
// 🔳 ⁡⁢⁣⁣СОХРАНЕНИЕ СОСТОЯНИЯ (State)⁡
{
	// ⭐ ⁡⁣⁣⁢Критически важно при трансформациях, чтобы не сломать последующую отрисовку.⁡
	// ☑️ ctx.save() ⁡⁣⁣⁢— Сохраняет текущее состояние (стили, трансформации) в стек.⁡
	// ☑️ ctx.restore() ⁡⁣⁣⁢— Восстанавливает последнее сохраненное состояние.⁡
	ctx.save();
	ctx.translate(100, 100);
	ctx.rotate(Math.PI / 4);
	ctx.fillRect(0, 0, 50, 50); // Нарисует повернутый квадрат
	ctx.restore(); // Вернет координаты и угол назад
}
