{
	// ☑️ ctx.globalAlpha ⁡⁣⁣⁢— Общая прозрачность (от 0.0 до 1.0).⁡
	// ☑️ ctx.globalCompositeOperation ⁡⁣⁣⁢— Как новый рисунок накладывается на старый.⁡
	{
		// ✅ 'source-over' ⁡⁣⁣⁢(по умолчанию) — поверх.⁡
		// ✅ 'destination-over' ⁡⁣⁣⁢— под.⁡
		// ✅ 'source-in' ⁡⁣⁣⁢— только там, где уже есть рисунок.⁡
		// ✅ 'xor' ⁡⁣⁣⁢— инверсия пересечения.⁡
		// ✅ ⁡⁣⁣⁢и другие (всего около 15 режимов).⁡
	}
	// ⭐ ⁡⁣⁣⁢Обрезка (Clipping):⁡
	// ☑️ ctx.clip() ⁡⁣⁣⁢— Все последующие рисунки будут видны только внутри текущего пути.⁡
}
