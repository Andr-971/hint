// ⁡⁢⁣⁢РАБОТА С МАССИВАМИ (методы Array.prototype)⁡
{
	[1, 2, 3].every((el) => el > 0); // true //* проверяет, соответствуют ли все элементы массива заданному условию, проходит по всему массиву и возвращает булево значение true, если каждый элемент удовлетворяет условию. Если есть хотя бы одно несовпадение — метод every() вернёт false.
	[1, 2, 3].some((el) => el < 0); // false //* проверяет, выполняется ли условие хотя бы для одного элемента массива, после перебора массива возвращает true, если хотя бы один элемент соответствует условию. Если совпадений нет — метод вернёт false
	[1, 2, 3, 4, 5, 6].filter((el) => el % 2 === 0); // [2, 4, 6] //* возвращает массив по условию, перебирает массив и выбирает из него элементы, которые проходят проверку по заданному условию.
	[5, 12, 8, 130, 12, 44].find((value) => value > 10); // 12 //* Возвращает первый элемент в массиве, который удовлетворяет условию поиска, Если нужный элемент будет найден, то find() его вернёт, если нет то вернётся undefined
	[1, 2, 3].findLast((x) => x > 1); // 3 //* findLast() возвращает последний подходящий элемент, если поддерживается.
	["apple", "banana", "orange"].includes("banana", 1); // true //* проверяет наличие элемента в массиве, возвращает булево значение true или false. Второй параметр 1 с какого индекса искать.
	[1, 2, 3, 4, 5].join(""); // "12345" //* возвращает представление массива в виде строки, состоящей из значений всех элементов массива, разделённых запятой или другим указанным разделителем. Без сепаратора, через запятую.
	["banana", "orange"].unshift("apple"); // ['apple', 'banana', 'orange'] //* добавляет элементы в начало массива
	["banana", "orange"].push("apple"); // ["banana", "orange", "apple"] //* добавляет элементы в конец массива
	["apple", "banana", "orange"].shift(); // "apple" //* удаляет и возвращает первый элемент массива
	["banana", "orange", "apple"].pop(); // "apple" //* извлекает элементы из конца массива
	["a", "b", "c", "d", "e"].slice(0, 3); // ['a', 'b', 'c'] //* копирует элементы одного массива  и создаёт новый массив, start — начало индекса, end — конец индекса не включая его. -1 последний.
	["red", "blue"].splice(1, 0, "green", "yellow"); // ['red', 'green', 'yellow', 'blue'] //* добавляет, удаляет или заменяет элементы из любой части массива, возвращает не удалённые. start_i — c какого индекса, включая его; delete_i — сколько индексов удалять, если 0 не удаляется; item1, item2 — какие элементы добавить.
	["red", "blue"].toSpliced(1, 0, "green", "yellow"); // ['red', 'green', 'yellow', 'blue'] //* — метод массива. Он позволяет: удалять элементы; добавлять элементы; заменять элементы. Не изменяет исходный массив.
	[1, 2, 3, 4, 5].with(2, 99); // [1, 2, 99, 4, 5] //* — экспериментальный метод, предложенный в рамках ECMAScript для работы с массивами. Он позволяет: создать новый массив на основе существующего; заменить один элемент в новой копии массива; не изменять исходный массив (иммутабельность).
	[1, 2, 3].concat([4, 5, 6]); // [1, 2, 3, 4, 5, 6] //* копирует элементы нескольких массивов, объединяет несколько массивов в один.(иммутабельность).
	[1, 2, 3, 4, 5].map((el) => el * 2); // [2, 4, 6, 8, 10]; //* возвращает массив на основе исходного
	["apple", "banana", "orange", "banana"].indexOf("banana", 2); // 3 //* возвращает индекс искомого элемента, принимает два аргумента: искомый элемент и индекс, с которого должен начинаться поиск. Если в массиве несколько одинаковых элементов, то indexOf вернёт только первый индекс. В случае если нужного элемента не будет, метод вернёт отрицательный индекс со значением -1
	[1, 3, 5, 8, 9, 10].findIndex((el) => el % 2 === 0); // 3 (индекс числа 8) //* возвращает индекс первого элемента массива, удовлетворяющего условиям заданной функции, или возвращает -1, если ни один из элементов не удовлетворяет функции.
	[10, 5, 20, 1].sort((a, b) => a - b); // [1, 5, 10, 20] //* возвращает отсортированный массив. То есть после применения метода исходный массив изменится. По умолчанию элементы сортируются в лексикографическом порядке — как строки. Если в массиве будут числа, то метод sort() преобразует их в строки и после отсортирует по кодам символов Unicode.
	[1, 2, 3, 4, 5].reverse(); // [5, 4, 3, 2, 1] //* разворачивает массив, меняет порядок элементов
	[2, 3, 4].reduce((acc, curr) => acc * curr, 1); // 24 //* свёртывает элементы массива в единое значение, применяет функцию обратного вызова последовательно для каждого элемента массива, сохраняет промежуточный результат и выводит общее значение. Это может быть объединённые строки, сумма чисел и так далее.
	["<section>", "<p>", "Hello!", "</p>", "</section>"].reduceRight((acc, tag) => tag + acc, ""); // '<section><p>Hello!</p></section>' //* — метод массива, который сводит массив к одному значению, перебирая элементы справа налево (от последнего к первому).
}
// ⁡⁢⁣⁢МЕТОДЫ КОНСТРУКТОРА Object⁡
{
	Object.entries({ name: "Alice", age: 25 }); // [['name', 'Alice'], ['age', 25]] //* возвращает массив пар ключ-значение объекта. Это позволяет одновременно работать с ключами и значениями
	Object.keys({ name: "Alice", age: 25 }); // ['name', 'age'] //* Возвращает массив, содержащий имена всех собственных перечислимых ключей переданного объекта.
	Object.values({ name: "Alice", age: 25 }); // ["Alice", 25] //* Возвращает массив, содержащий имена всех собственных перечислимых значений переданного объекта.
	Object.assign({}, { a: 1, b: 2 }); // { a: 1, b: 2 } //* Создаёт новый объект путём копирования значений всех собственных перечислимых свойств из одного или более исходных объектов в целевой объект.
	Object.create(); //* Создаёт новый объект с указанными объектом прототипа и свойствами.
	Object.defineProperty({}, "name", {
		value: "Alice",
		writable: true,
		enumerable: true,
		configurable: true,
	}); //* Добавляет к объекту именованное свойство, описываемое переданным дескриптором. false - по умолчанию
	Object.defineProperties(); //* Добавляет к объекту именованные свойства, описываемые переданными дескрипторами.
	Object.getOwnPropertyDescriptor({ name: "Alice" }, "name"); // { value: "Alice",	writable: true,	enumerable: true, configurable: true } //* Что делает: возвращает дескриптор свойства prop объекта obj (или undefined, если свойства нет).
	Object.getOwnPropertyDescriptors({ name: "Alice" }); // {  name: { value: "Alice", writable: true, enumerable: true, configurable: true }} //* Возвращает все дескрипторы свойств объекта.
	Object.preventExtensions(obj); //* запрещает добавление новых свойств; существующие свойства можно изменять;
	Object.isExtensible(obj); //* — true, если можно добавлять свойства;
	Object.isSealed(obj); //* — true, если нельзя добавлять/удалять и configurable: false
	Object.isFrozen(obj); //* — true, если нельзя добавлять/удалять/изменять и configurable: false, writable: false.
	Object.seal(); //* Запечатывает объект, ограничивая возможность его модификации: запрещает добавление новых свойств; запрещает удаление существующих свойств; делает все существующие свойства неконфигурируемыми (configurable: false). При этом значения существующих свойств можно изменять, если они остаются записываемыми (writable: true).
	Object.freeze(obj); //* «замораживает» объект: нельзя добавить/удалить свойства, изменить их дескрипторы или значения (если свойство writable: false). Возвращает: тот же объект.
	Object.getOwnPropertySymbols(); //* Возвращает массив всех символьных свойств, найденных непосредственно в переданном объекте.
	Object.getPrototypeOf(); //* Возвращает прототип указанного объекта.
	Object.setPrototypeOf(obj, prototype); //* Устанавливает или изменяет прототип указанного объекта.
	Object.is(); //* Определяет, являются ли два значения различимыми (то есть, одинаковыми)
	Object.fromEntries([
		["a", 1],
		["b", 2],
	]); // { a: 1, b: 2 } //* Что делает: преобразует итерируемый объект пар (например, массив массивов [ключ, значение]) в объект.
	Object.groupBy([1, 2, 6, 5, 4, 9], (n) => (n % 2 === 0 ? "even" : "odd")); // { odd: [1, 5, 9], even: [2, 6, 4] } //* — статический метод для группировки элементов итерируемого объекта (чаще всего массива) на основе значений, возвращаемых заданной функцией‑колбэком. Параметры: items — итерируемый объект (обычно массив), элементы которого нужно сгруппировать; callbackFn — функция, выполняемая для каждого элемента. Должна возвращать значение (строку или символ), которое будет использовано как ключ группы.
}
