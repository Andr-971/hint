// ⁡⁢⁣⁢PROXY — механизм для создания обёртки над объектом, перехватывающей основные операции с ним. Позволяет контролировать чтение, запись, вызов методов и другие действия.⁡
{
	const user = { name: "Alice", age: 30 };
	const userProxy = new Proxy(user, {
		get(target, property) {
			console.log(`Чтение свойства "${property}"`);
			return target[property];
		},
		set(target, property, value) {
			console.log(`Запись свойства "${property}" = ${value}`);
			target[property] = value;
			return true;
		},
	});

	userProxy.name; // «Чтение свойства "name"»
	userProxy.city = "Москва"; // «Запись свойства "city" = Москва
}
