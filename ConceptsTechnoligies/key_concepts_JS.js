		// ⭐ ⁡⁣⁣⁢Некоторые особенности использования async/await:⁡
		// ⁡⁣⁢⁢- async⁡⁡ ⁡⁢⁢⁢функция всегда возвращает Promise. Если функция возвращает значение, оно автоматически оборачивается в промис. Если функция выбрасывает ошибку, она будет отклонена.⁡
		// ⁡⁣⁢⁢- await⁡ ⁡⁢⁢⁢приостанавливает выполнение функции до разрешения Promise. Это позволяет писать асинхронный код в более линейном и понятном виде.⁡
