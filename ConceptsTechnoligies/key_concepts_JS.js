	// ⁡⁢⁣⁡⁢⁣⁣Некоторые методы промисов (Promise) в JavaScript:⁡
	// ⁡⁣⁢⁢then().⁡ ⁡⁣⁣⁢Используют, чтобы выполнить код после успешного выполнения асинхронной операции. Принимает в качестве аргумента две функции-колбэка. Если промис в состоянии fulfilled, то выполнится первая функция, если в состоянии rejected — вторая.⁡
