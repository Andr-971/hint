		// ⁡⁢⁣⁣- Использование математических функций⁡
		// ⁡⁢⁣⁣- Преобразование NodeList в массив⁡
		// ⁡⁢⁣⁣- Использование массива в качестве аргументов⁡
		// ⁡⁢⁣⁣- Добавление элемента в список⁡
		// ⁡⁢⁣⁣- Объединение объектов⁡
