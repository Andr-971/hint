		// ⁡⁣⁣⁢Значение ⁡⁣⁢⁢this⁡ ⁡⁣⁣⁢зависит от того, как вызывается функция:⁡
		// ⁡⁣⁣⁢Глобальный контекст. В этом контексте this относится к глобальному объекту, который в браузерах — это window, а в Node.js — global. ⁡
		// ⁡⁣⁣⁢Контекст функции. Внутри функции this относится к объекту, методом которого является функция. ⁡
		// ⁡⁣⁣⁢Контекст конструктора. Когда функция используется в качестве конструктора с ключевым словом new, this ссылается на вновь созданный объект.⁡
		// ⁡⁣⁣⁢Обработчики событий. В обработчиках событий this обычно относится к элементу, который вызвал событие.⁡
		// ⁡⁣⁣⁢Методы Call, Apply и Bind. Эти методы позволяют явно задавать значение this при вызове функции. ⁡
		// ⁡⁣⁣⁢Важно отметить, что this — не переменная, а ключевое слово, значение которого нельзя изменить.⁡
		// ⁡⁣⁣⁢Вызов без объекта: this == undefined⁡
		// ⁡⁣⁣⁢Стрелочные функции особенные: у них нет своего «собственного» this. Если мы ссылаемся на this внутри такой функции, то оно берётся из внешней «нормальной» функции.⁡
		// ⁡⁣⁢⁢Метод call()⁡ ⁡⁣⁣⁢вызывает функцию с заданным значением this и аргументами, переданными через запятую. Это позволяет явно указывать контекст, в котором должна выполняться функция. Например, метод call() часто используется для наследования методов одного объекта другим.⁡
		func.call(this, arg1, arg2);
