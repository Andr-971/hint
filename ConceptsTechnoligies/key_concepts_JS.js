		// ⚪ ⁡⁣⁣⁢Uint16Array — это типизированный массив, который представляет массив 16-битных беззнаковых целых чисел. Каждый элемент может принимать значение от 0 до 65535.⁡
		// ⚪ ⁡⁣⁣⁢Int32Array — это типизированный массив 32-битных целых чисел со знаком. Он позволяет работать с двоичными данными в буфере, что удобно для обработки больших наборов числовых данных.⁡
		// ⚪ ⁡⁣⁣⁢Uint32Array — это типизированный массив, который представляет массив 32-битных беззнаковых целых чисел. Каждый элемент может хранить число от 0 до 4 294 967 295.⁡
