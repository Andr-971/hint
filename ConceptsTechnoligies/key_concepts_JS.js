	// ⁡⁢⁢⁢Миксин (или примесь от англ. mixin)⁡ ⁡⁣⁣⁢— это способ комбинировать функциональность разных классов или объектов без использования наследования. То есть это функция, которая принимает в качестве аргумента класс или объект и возвращает новый класс или объект с расширенной функциональностью. Миксин может копировать свойства и методы из других классов или объектов или добавлять новые.⁡
	// ⁡⁢⁢⁢Композиция⁡ ⁡⁣⁣⁢— это подход, при котором объекты создаются путём объединения нескольких объектов или функций для формирования более сложного поведения. Композиция позволяет строить системы из небольших, независимых модулей, что делает код более гибким и легко расширяемым.⁡
