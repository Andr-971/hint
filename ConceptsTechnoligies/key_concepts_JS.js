	// 🔳 ⁡⁣⁣⁡⁣⁣⁢Основные отличия var, let и const в JavaScript:⁡
	// ⁡⁣⁢⁡⁣⁢⁢var ⁡⁣⁣⁢- объявляет переменные с функцией или функциональной(глобальной) областью видимости. Переменные, объявленные с помощью var, могут быть переопределены и повторно объявлены.⁡
	// ⁡⁣⁢⁡⁣⁢⁢let⁡ ⁡⁣⁣⁢- объявляет переменные с областью действия блока. Переменные, объявленные с помощью let, доступны только внутри блока, в котором они объявлены (например, внутри if, for, while и т. д.). К переменной let нельзя обращаться до момента объявления.⁡
	// ⁡⁣⁢⁢const⁡ ⁡⁣⁣⁢- объявляет переменные с блочной областью действия. Переменные, объявленные с помощью const, не могут быть переназначены. Значение переменной должно быть присвоено сразу в момент объявления⁡.
	// ⚠️ ⁢Таким образом, var используется, когда нужна функциональная область видимости и переменная должна быть доступна внутри всей функции, let — когда нужна блочная область видимости и переменная может быть переназначена, а const — для объявления констант, которые не должны изменяться⁡
