		// ⁡⁣⁣⭐ ⁡⁣⁣⁢Упрощение обработки ошибок. Обработка ошибок с использованием async/await становится проще и интуитивно понятной благодаря конструкции try/catch.⁡
		// ⁡⁣⁣⁢⭐ Снижение вложенности. Async/await помогает избежать так называемого «адского коллбэка» (callback hell), где код становится трудно читаемым из-за множества вложенных функций.⁡
