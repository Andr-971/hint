	// ⁡⁣⁡⁣⁢⁢resolve(value)⁡⁡ ⁡⁣⁢⁢и Promise.reject(reason).⁡ ⁡⁣⁣⁢Создают промисы, которые немедленно разрешаются или отклоняются соответственно.⁡
