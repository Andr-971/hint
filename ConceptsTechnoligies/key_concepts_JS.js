	// 🔳 ⁡⁢⁣⁣ЧИСЛА И ДАТЫ (Объекты, имеющие дело с числами, датами и математическими вычислениями)⁡
	{
		// ⚪ ⁡⁣⁣⁢Number  — это основной тип данных для работы с числами, охватывающий как целые, так и дробные значения, включая специальные числа, такие как NaN (не число), Infinity и -Infinity.Он использует 64-битное представление для точности и поддерживает разные системы счисления. ⁡
		// ⚪ ⁡⁣⁣⁢Math — это всесторонняя и удобная математическая библиотека в javascrript⁡
		// ⚪ ⁡⁣⁣⁢Date — это встроенный объект для работы с датой и временем. Он содержит методы для создания, хранения, изменения или вывода элементов времени с точностью до миллисекунды.К таким элементам относят: год, месяц, день месяца, неделя, час, минута, секунда, миллисекунда, день недели.⁡
	}
	// 🔳 ⁡⁢⁣⁡⁢⁣⁣ОБРАБОТКА ТЕКСТА (String, RegExp)⁡
	{
		// ⚪ ⁡⁣⁣⁢String — это тип данных для представления произвольной последовательности символов.⁡
		// ⚪ ⁡⁣⁣⁢RegExp — это объект регулярного выражения с предопределёнными свойствами и методами. Он используется для поиска комбинаций символов в строках.Регулярные выражения позволяют находить и заменять текстовые фрагменты, проверять соответствие строк определённым шаблонам и многое другое.⁡
	}
