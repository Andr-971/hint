[2, 5, 9].lastIndexOf(5)                   // ⁡⁢⁢⁢5⁡                 ⁡⁣⁡⁣⁣⁢Поиск значения справа.⁡⁡
[1, 2, 3].join('-')                       // ⁡⁢⁢⁢"1-2-3"⁡            ⁡⁣⁣⁢Склеивает элементы в строку с разделителем.⁡
[1, 2, 3].toString()                      // ⁡⁢⁢⁢"1,2,3"⁡           ⁡⁣⁣⁢Преобразует массив в строку через запятую.⁡
[1, [2, [3]]].flat(2)                     // ⁡⁢⁢⁢[1, 2, 3]⁡         ⁡⁣⁣⁢Разворачивает вложенные массивы на указанную глубину.⁡
