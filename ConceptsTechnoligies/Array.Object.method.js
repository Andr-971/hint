let b = [...["a", "b"].keys()];           // ⁡⁢⁢⁢[0, 1]⁡              ⁡⁣⁣⁢Возвращает массив индексов.⁡
let с = [...["a", "b"].values()];         // ⁡⁢⁢⁢['a','b']⁡           ⁡⁣⁣⁡⁣⁣⁢Возвращает массив значений.⁡⁡ 
