//                                                              ⁡⁣⁣⁢применяет функцию обратного вызова последовательно⁡
//                                                              ⁡⁣⁣⁢для каждого элемента массива, сохраняет промежуточный⁡
//                                                              ⁡⁣⁣⁢результат и выводит общее значение.⁡
[2, 3, 4].reduceRight((acc, curr) => acc * curr, 1) // ⁡⁢⁢⁢24⁡⁡     ⁡⁣⁣⁢  Сворачивает массив справа налево.⁡
[1, 2].forEach((el, i) => console.log(el, i)) // ⁡⁤⁤⁥undefine ⁡      ⁡⁣⁡⁣⁣⁢Выполняет функцию для каждого элемента.⁡
