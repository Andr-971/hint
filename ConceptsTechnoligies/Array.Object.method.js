//														   ⁡⁣⁣⁢ind, сколько ind, если 0 не удаляется, какие el⁡
[1, 2, 3].reverse()             // ⁡⁢⁣⁢[3, 2, 1]⁡⁡               ⁡⁣⁣⁢Переворачивает порядок элементов.⁡   
[3, 1, 2].sort((a, b) => a - b) // ⁡⁢⁣⁢[1, 2, 3]⁡               ⁡⁣⁣⁢Сортирует элементы на месте (по умолчанию как строки).⁡
[1, 2, 3].fill("X", 1, 2);      // ⁡⁢⁣⁢[1, 'X', 3]⁡             ⁡⁣⁣⁢Заполняет все элементы массива (или часть элементов)⁡
//                                                         ⁡⁣⁣⁡⁣⁣⁢статическим значением. Заполняет индексы 2, до 3⁡
[1, 2, 3, 4, 5].copyWithin(0, 3, 5);// ⁡⁢⁣⁡⁢⁣⁢[4, 5, 3, 4, 5]⁡     ⁡⁣⁣⁢Копирует часть массива в другое место. Скопировать⁡
//                                                         ⁡⁣⁣⁢с 4 индекса по 5, не включая его, начиная c 0 индекса.⁡
// 🔳 ⁡⁢⁣⁣НЕ ИЗМЕНЯЮТ ИСХОДНЫЙ МАССИВ (немутирующие)⁡
// ⁡⁢⁣⁣МЕТОД	                            ВОЗВРАЩАЕТ	            КРАТКОЕ ОПИСАНИЕ⁡
[1, 2].concat([3])                      // ⁡⁢⁢⁢[1,2,3]⁡             ⁡⁣⁣ ⁡⁣⁣⁢Объединяет массивы / значения.⁡
["a", "b", "c", "d", "e"].slice(0, 3)   // ⁡⁣⁣⁡⁢⁢⁢['a', 'b', 'c']⁡⁡     ⁡⁣⁣ ⁡⁣⁣⁢Возвращает новый массив – срез оригинала.⁡
//                                                             ⁡⁣⁣⁡⁣⁣ ⁡⁣⁣⁢3 - индекс не влючая его.⁡
[1, 2, 3].map((el) => el * 2)           // ⁡⁢⁢⁢[2, 4, 6]⁡            ⁡⁣⁣⁢Преобразует каждый элемент по функции.⁡
