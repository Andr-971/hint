// 🔳 ⁡⁢⁣⁣БАЗОВЫЕ КОМАНДЫ⁡
{
	// npm init ⁡⁣⁣⁢— Инициализирует новый проект Node.js: создаёт файл package.json с вопросами о настройках проекта. Опция -y или --yes — автоматически заполняет поля значениями по умолчанию: npm init -y.⁡
	// npm install (или npm i) ⁡⁣⁣⁢— Устанавливает все зависимости, перечисленные в package.json, в папку node_modules. С именем пакета — устанавливает конкретный пакет: npm install <package_name>.⁡
	// npm uninstall (или npm rm, npm remove) ⁡⁣⁣⁢— Удаляет пакет из проекта и обновляет package.json: npm uninstall <package_name>.⁡
	// npm update ⁡⁣⁣⁢— Обновляет пакеты до последних версий, учитывая ограничения в package.json. Для конкретного пакета: npm update <package_name>.⁡
	// npm run <script> ⁡⁣⁣⁢— Запускает пользовательский скрипт, определённый в разделе scripts файла package.json: npm run dev.⁡
	// npm test ⁡⁣⁣⁢— Запускает тесты, как указано в разделе scripts файла package.json (обычно соответствует npm run test).⁡
	// npm publish ⁡⁣⁣⁢— Публикует пакет в реестре npm для общего доступа (требуется авторизация через npm login).⁡
}
// 🔳 ⁡⁢⁣⁣УПРАВЛЕНИЕ ЗАВИСИМОСТЯМИ⁡
{
	// npm install <package> --save (или -S) ⁡⁣⁣⁢— Устанавливает пакет и добавляет его в раздел dependencies в package.json (для продакшен‑зависимостей).⁡
	// npm install <package> --save-dev (или -D) ⁡⁣⁣⁢— Устанавливает пакет как зависимость разработки и добавляет в devDependencies (например, ESLint, Webpack): npm install eslint --save-dev.⁡
	// npm install -g <package> ⁡⁣⁣⁢— Устанавливает пакет глобально (доступен во всей системе), обычно для утилит командной строки: npm install nodemon -g.⁡
	// npm outdated ⁡⁣⁣⁢— Проверяет наличие устаревших пакетов в проекте и показывает доступные обновления.⁡
	// npm list (или npm ls) ⁡⁣⁣⁢— Выводит список установленных пакетов и их зависимостей в виде дерева. С флагом -g — показывает глобально установленные пакеты: npm list -g. С флагом --depth=0 — ограничивает вывод только корневыми пакетами: npm list --depth=0⁡
}
// 🔳 ⁡⁢⁣⁣РАБОТА С ОКРУЖЕНИЕМ И КЭШЕМ⁡
{
	// npm cache clean --force ⁡⁣⁣⁢— Очищает кэш npm, что может помочь при проблемах с установкой пакетов.⁡
	// npm cache verify ⁡⁣⁣⁢— Проверяет целостность кэша npm (рекомендуется вместо полной очистки).⁡
    // npm config ⁡⁣⁣⁢— Управляет настройками npm. Примеры: npm config list — показать текущие настройки; npm config set registry <url> — изменить реестр пакетов.⁡
}
// 🔳 ⁡⁢⁣⁣АУДИТ И БЕЗОПАСНОСТЬ⁡
{
	// npm audit ⁡⁣⁣⁢— Анализирует зависимости на наличие уязвимостей и предлагает способы их устранения.⁡
	// npm audit fix ⁡⁣⁣⁢— Aвтоматически исправляет обнаруженные проблемы (если возможно).⁡
	// npm doctor ⁡⁣⁣⁢— Проверяет окружение Node.js и npm на наличие распространённых проблем (например, права доступа, сетевые настройки).⁡
}
// 🔳 ⁡⁢⁣⁣ИНФОРМАЦИЯ И ПОМОЩЬ⁡
{
	// npm help <command> ⁡⁣⁣⁢— Показывает справку по конкретной команде: npm help install.⁡
	// npm version ⁡⁣⁣⁢— Обновляет версию проекта в package.json согласно семантическому версионированию (patch, minor, major): npm version patch.⁡
	// npm whoami ⁡⁣⁣⁢— Показывает имя текущего пользователя, авторизованного в npm.⁡
	// npm login / npm logout ⁡⁣⁣⁢— Авторизуется в реестре npm (npm login) или выходит из учётной записи (npm logout).⁡
}
// 🔳 ⁡⁢⁣⁣ДОПОЛНИТЕЛЬНЫЕ ПОЛЕЗНЫЕ КОМАНДЫ⁡
{
	// npm link ⁡⁣⁣⁢— Создаёт символическую ссылку на локальный пакет, чтобы использовать его в других проектах без публикации. В папке пакета: npm link. В проекте, где нужно использовать пакет: npm link <package-name>.⁡
	// npm prune ⁡⁣⁣⁢— Удаляет неиспользуемые пакеты (те, что есть в node_modules, но отсутствуют в package.json).⁡
	// npm outdated --depth=0 ⁡⁣⁣⁢— Показывает устаревшие пакеты верхнего уровня (без вложенных зависимостей).⁡
	// npm dist-tags ⁡⁣⁣⁢— Управляет тегами версий пакета в реестре (например, latest, beta): npm dist-tags ls <package> — список тегов; npm dist-tags add <package>@<version> <tag> — добавить тег.⁡
}